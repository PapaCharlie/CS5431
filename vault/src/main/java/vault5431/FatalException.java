public class FatalException extends Exception {

    public FatalException() {
        super();
    }

    public FatalException(Throwable cause) {
        super(cause);
    }

    public FatalException(String message) {
        super(message);
    }

    public FatalException(String message, Throwable cause) {
        super(message, cause);
    }

}

